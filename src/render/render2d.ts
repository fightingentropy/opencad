const drawSectionMarker = (
  ctx: CanvasRenderingContext2D,
  e: AnyEntity,
  _color: string,
  lineWidth: number,
  state: DrawState
) => {
  const a = e.a ?? { x: 0, y: 0 };
  const b = e.b ?? { x: a.x + 50, y: a.y };
  const stroke = strokeColorFor(state, '#1f2229');

  ctx.save();
  ctx.strokeStyle = stroke;
  ctx.fillStyle = stroke;
  ctx.lineWidth = Math.max(0.8, lineWidth * 1.6);
  ctx.beginPath();
  ctx.moveTo(a.x, a.y);
  ctx.lineTo(b.x, b.y);
  ctx.stroke();

  // Arrows at each end pointing in viewDirection
  const dir = e.viewDirection ?? Math.atan2(b.y - a.y, b.x - a.x) + Math.PI / 2;
  const arrLen = 4;
  for (const p of [a, b]) {
    const tipX = p.x + Math.cos(dir) * arrLen;
    const tipY = p.y + Math.sin(dir) * arrLen;
    ctx.beginPath();
    ctx.moveTo(p.x, p.y);
    ctx.lineTo(tipX, tipY);
    ctx.stroke();
    drawArrowhead(ctx, tipX, tipY, dir, 1.5);
  }

  // Bubble at midpoint
  const cx = (a.x + b.x) / 2;
  const cy = (a.y + b.y) / 2;
  ctx.fillStyle = '#0d131c';
  ctx.beginPath();
  ctx.arc(cx, cy, 4, 0, Math.PI * 2);
  ctx.fill();
  ctx.stroke();
  ctx.restore();

  if (e.label) {
    drawWorldText(ctx, e.label, cx, cy, 3, '#e6e8ec', 'center', 'middle', 'ui-monospace, monospace');
  }
};

