/** Compute a generous bounds rectangle for any new-kind entity. */
const newKindBounds = (e: AnyEntity): { minX: number; minY: number; maxX: number; maxY: number } | null => {
  const expand = (b: { minX: number; minY: number; maxX: number; maxY: number }, p: Vec2) => {
    if (p.x < b.minX) b.minX = p.x;
    if (p.y < b.minY) b.minY = p.y;
    if (p.x > b.maxX) b.maxX = p.x;
    if (p.y > b.maxY) b.maxY = p.y;
  };
  const b = { minX: Infinity, minY: Infinity, maxX: -Infinity, maxY: -Infinity };
  if (e.position) expand(b, e.position);
  if (e.a) expand(b, e.a);
  if (e.b) expand(b, e.b);
  if (e.points) for (const p of e.points) expand(b, p);
  if (!isFinite(b.minX)) return null;
  // Pad single-point entities so the selection box reads
  if (e.size || e.width || e.height) {
    const pad = Math.max(e.size ?? 0, e.width ?? 0, e.height ?? 0) * 0.5;
    b.minX -= pad; b.minY -= pad; b.maxX += pad; b.maxY += pad;
  } else if (b.minX === b.maxX && b.minY === b.maxY) {
    const pad = 5;
    b.minX -= pad; b.minY -= pad; b.maxX += pad; b.maxY += pad;
  }
  return b;
};

/** Selection box for new-kind entities. Mirrors drawSelectionBox style. */
const drawSelectionBoxFor = (ctx: CanvasRenderingContext2D, e: AnyEntity) => {
  const bb = newKindBounds(e);
  if (!bb) return;
  const pad = 1.5;
  ctx.save();
  ctx.strokeStyle = SELECTION_COLOR;
  ctx.lineWidth = 0.3;
  ctx.setLineDash([1.2, 1]);
  ctx.strokeRect(
    bb.minX - pad,
    bb.minY - pad,
    bb.maxX - bb.minX + pad * 2,
    bb.maxY - bb.minY + pad * 2
  );
  ctx.setLineDash([]);
  ctx.restore();
};

const strokeColorFor = (state: { isSelected: boolean; isHovered: boolean }, base: string): string =>
  state.isSelected ? SELECTION_COLOR : state.isHovered ? HOVER_COLOR : base;

/** Render text in the y-flipped world frame. */
const drawWorldText = (
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  size: number,
  color: string,
  align: CanvasTextAlign = 'center',
  baseline: CanvasTextBaseline = 'middle',
  font = 'sans-serif'
) => {
  ctx.save();
  ctx.translate(x, y);
  ctx.scale(1, -1);
  ctx.fillStyle = color;
  ctx.font = `${size}px ${font}`;
  ctx.textAlign = align;
  ctx.textBaseline = baseline;
  ctx.fillText(text, 0, 0);
  ctx.restore();
};

// ----- fitting -----------------------------------------------------------

const drawFitting = (
  ctx: CanvasRenderingContext2D,
  e: AnyEntity,
  color: string,
  lineWidth: number,
  state: DrawState
) => {
  const pos = e.position ?? { x: 0, y: 0 };
  const rot = e.rotation ?? 0;
  const w = Math.max(4, e.width ?? e.size ?? 8);
  const stroke = strokeColorFor(state, color);

  ctx.save();
  ctx.translate(pos.x, pos.y);
  ctx.rotate(rot);
  ctx.strokeStyle = stroke;
  ctx.fillStyle = stroke;
  ctx.lineWidth = lineWidth;

  const half = w / 2;
  switch (e.fittingKind) {
    case 'flat-bend':
    case 'inside-riser':
    case 'outside-riser':
      ctx.beginPath();
      ctx.arc(0, 0, half, 0, Math.PI / 2);
      ctx.stroke();
      break;
    case 'tee':
      ctx.beginPath();
      ctx.moveTo(-half, 0);
      ctx.lineTo(half, 0);
      ctx.moveTo(0, 0);
      ctx.lineTo(0, half);
      ctx.stroke();
      break;
    case 'cross':
      ctx.beginPath();
      ctx.moveTo(-half, 0);
      ctx.lineTo(half, 0);
      ctx.moveTo(0, -half);
      ctx.lineTo(0, half);
      ctx.stroke();
      break;
    case 'reducer':
      ctx.beginPath();
      ctx.moveTo(-half, -half);
      ctx.lineTo(half, -half * 0.5);
      ctx.lineTo(half, half * 0.5);
      ctx.lineTo(-half, half);
      ctx.closePath();
      ctx.stroke();
      break;
    case 'end-cap':
      ctx.strokeRect(-half, -half, w, w);
      ctx.beginPath();
      ctx.moveTo(half, -half);
      ctx.lineTo(half, half);
      ctx.lineWidth = lineWidth * 2;
      ctx.stroke();
      break;
    case 'coupler':
      ctx.strokeRect(-half, -half * 0.4, w, w * 0.4);
      break;
    case 'end-plate':
      ctx.strokeRect(-half * 0.6, -half * 0.6, w * 0.6, w * 0.6);
      break;
    default:
      // Generic glyph — small diamond
      ctx.beginPath();
      ctx.moveTo(0, -half);
      ctx.lineTo(half, 0);
      ctx.lineTo(0, half);
      ctx.lineTo(-half, 0);
      ctx.closePath();
      ctx.stroke();
  }
  ctx.restore();

  // Hover/select label with the fittingKind code (e.g. "B45")
  if (state.isSelected || state.isHovered) {
    const code = (e.fittingKind ?? '').slice(0, 6).toUpperCase();
    if (code) {
      drawWorldText(ctx, code, pos.x, pos.y - w * 0.9, Math.max(2.4, w * 0.4), '#9aa3b2', 'center', 'top', 'ui-monospace, monospace');
    }
  }
};

